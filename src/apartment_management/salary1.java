    public ArrayList<payment> userlist() {
        ArrayList<payment> userlist = new ArrayList<>();
        try {
            conn = dbconnection.getConnetion();
            String query1 = ("SELECT * from employee_salary ");
            java.sql.Statement st = conn.createStatement();
            ResultSet RS = st.executeQuery(query1);
            payment payment;
            while (RS.next()) {
                payment = new payment(RS.getString("userid"), RS.getString("Date"), RS.getString("salary"));
                userlist.add(payment);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
        return userlist;
    }

