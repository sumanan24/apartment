    private void comloadowner() {
        DefaultListModel Model = new DefaultListModel();
        l1.setModel(Model);
        try {
            conn = dbconnection.getConnetion();
            PST = conn.prepareStatement("select distinct userid from employee where userid like '" + userid.getText() + "%' ");
            RS = PST.executeQuery();
            while (RS.next()) {
                String did = RS.getString("userid");
                Model.addElement(did);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);

        }
    }

    private void showdwp() {
        ArrayList<payment> list = userlist();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        Object[] row = new Object[3];

        for (int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getempid();
            row[1] = list.get(i).getdate();
            row[2] = list.get(i).getsalary();

            model.addRow(row);
        }
    }
